"""Dataset"""

# ============================================================
# Converts each name into sequence of indices
# ============================================================
class NameDataset(Dataset):
    def __init__(self, names):
        self.data = []
        for name in names:
            seq = [SOS] + [c2i[c] for c in name] + [EOS]
            self.data.append(torch.tensor(seq))

    def __len__(self):
        return len(self.data)

    def __getitem__(self, idx):
        return self.data[idx]

# Pads sequences to same length in a batch
def collate_fn(batch):
    max_len = max(len(x) for x in batch)
    padded = torch.stack([
        torch.cat([x, torch.full((max_len-len(x),), PAD)])
        for x in batch
    ])
    return padded

dataset = NameDataset(names)
loader = DataLoader(dataset, batch_size=64, shuffle=True, collate_fn=collate_fn)

"""Hyperparameters"""

EMBED = 64
HIDDEN = 128
LAYERS = 2
LR = 0.003
EPOCHS = 20

"""# VANILLA RNN"""

class VanillaRNN(nn.Module):
    def __init__(self):
        super().__init__()
        # Embedding layer (character to vector)
        self.embed = nn.Embedding(vocab_size, EMBED, padding_idx=PAD)
        #rnn layer
        self.rnn = nn.RNN(EMBED, HIDDEN, LAYERS, batch_first=True, dropout=0.2)
        #output layer
        self.fc = nn.Linear(HIDDEN, vocab_size)

    def forward(self, x, h=None):
        x = self.embed(x)   #convert to embedding
        out, h = self.rnn(x, h) #pass through rnn
        out = self.fc(out)    #predict next char
        return out, h

    def init_hidden(self, b):
        return torch.zeros(LAYERS, b, HIDDEN).to(device)

    def generate(self, max_len=15, temp=0.8):
        self.eval()
        x = torch.tensor([[SOS]]).to(device)
        h = self.init_hidden(1)
        name = ""

        for _ in range(max_len):
            out, h = self.forward(x, h)
            #applying temperature sampling
            probs = torch.softmax(out[0, -1]/temp, dim=0)
            idx = torch.multinomial(probs, 1).item()
            if idx == EOS: break
            if idx not in [PAD, SOS]:
                name += i2c[idx]
            x = torch.tensor([[idx]]).to(device)
        return name

    def num_params(self):
        return sum(p.numel() for p in self.parameters() if p.requires_grad)

"""# BLSTM"""

class BLSTM(nn.Module):
    def __init__(self):
        super().__init__()
        self.embed = nn.Embedding(vocab_size, EMBED, padding_idx=PAD)
        #bidirectional lstm
        self.lstm = nn.LSTM(EMBED, HIDDEN, LAYERS,
                            batch_first=True,
                            bidirectional=True,
                            dropout=0.3)
        self.fc = nn.Linear(HIDDEN*2, vocab_size)

    def forward(self, x, h=None):
        x = self.embed(x)
        out, h = self.lstm(x, h)
        out = self.fc(out)
        return out, h

    def generate(self, max_len=15, temp=0.8):
        self.eval()
        x = torch.tensor([[SOS]]).to(device)
        h = None
        name = ""

        for _ in range(max_len):
            out, h = self.forward(x, h)
            probs = torch.softmax(out[0, -1]/temp, dim=0)
            idx = torch.multinomial(probs, 1).item()
            if idx == EOS: break
            if idx not in [PAD, SOS]:
                name += i2c[idx]
            x = torch.tensor([[idx]]).to(device)
        return name

    def num_params(self):
        return sum(p.numel() for p in self.parameters() if p.requires_grad)

"""# ATTENTION RNN"""

class Attention(nn.Module):
    def __init__(self):
        super().__init__()
        self.attn = nn.Linear(HIDDEN, HIDDEN)
        self.v = nn.Linear(HIDDEN, 1)

    def forward(self, rnn_out):
        scores = self.v(torch.tanh(self.attn(rnn_out)))
        weights = torch.softmax(scores, dim=1)
        context = torch.sum(weights * rnn_out, dim=1)
        return context


class AttnRNN(nn.Module):
    def __init__(self):
        super().__init__()
        self.embed = nn.Embedding(vocab_size, EMBED, padding_idx=PAD)
        self.rnn = nn.RNN(EMBED, HIDDEN, LAYERS, batch_first=True)
        self.attn = Attention()
        self.fc = nn.Linear(HIDDEN*2, vocab_size)

    def forward(self, x, h=None):
        x = self.embed(x)
        rnn_out, h = self.rnn(x, h)
        context = self.attn(rnn_out)
        context = context.unsqueeze(1).expand_as(rnn_out)
        out = torch.cat([rnn_out, context], dim=2)
        out = self.fc(out)
        return out, h

    def generate(self, max_len=15, temp=0.8):
        self.eval()
        x = torch.tensor([[SOS]]).to(device)
        h = None
        name = ""

        for _ in range(max_len):
            out, h = self.forward(x, h)
            probs = torch.softmax(out[0, -1]/temp, dim=0)
            idx = torch.multinomial(probs, 1).item()
            if idx == EOS: break
            if idx not in [PAD, SOS]:
                name += i2c[idx]
            x = torch.tensor([[idx]]).to(device)
        return name

    def num_params(self):
        return sum(p.numel() for p in self.parameters() if p.requires_grad)

"""# TRAINING FUNCTION"""

def train(model):
    model.to(device)
    optimizer = optim.Adam(model.parameters(), lr=LR)
    loss_fn = nn.CrossEntropyLoss(ignore_index=PAD)

    for epoch in range(EPOCHS):
        total_loss = 0

        for batch in loader:
            batch = batch.to(device)
            inp, tgt = batch[:, :-1], batch[:, 1:]

            out, _ = model(inp)

            loss = loss_fn(out.reshape(-1, vocab_size),
                           tgt.reshape(-1))

            optimizer.zero_grad()
            loss.backward()
            torch.nn.utils.clip_grad_norm_(model.parameters(), 5)
            optimizer.step()

            total_loss += loss.item()

        print(f"Epoch {epoch+1}: {total_loss:.3f}")

    return model

