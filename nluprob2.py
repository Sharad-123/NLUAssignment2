"""# Analyzing Realism"""

def is_realistic(name):
    vowels = "aeiou"

    # Conditions for realism
    if len(name) < 3 or len(name) > 12:
        return False

    vowel_count = sum(1 for c in name if c in vowels)
    if vowel_count == 0:
        return False

    if any(name.count(ch) > 3 for ch in set(name)):
        return False

    return True


def realism_score(samples):
    valid = sum(1 for n in samples if is_realistic(n))
    return valid / len(samples)


