"""# Categorizing Failure Types"""

def categorize_failures(samples):
    categories = {
        "too_short": 0,
        "repetition": 0,
        "non_alpha": 0
    }

    for n in samples:
        # Check if the name is too short
        if len(n) < 3:
            categories["too_short"] += 1
        # Check if the name contains non-alphabetic characters
        if not n.isalpha():
            categories["non_alpha"] += 1
        # Check for excessive character repetition
        if any(n.count(ch) > 3 for ch in set(n)):
            categories["repetition"] += 1

    return categories


