import torch.nn.functional as F

def get_similar_words(word, embeddings, word2idx, idx2word, top_k=5):

    if word not in word2idx:
        print(f"{word} not in vocabulary")
        return

    word_idx = word2idx[word]
    word_vec = embeddings[word_idx]

    similarities = F.cosine_similarity(word_vec.unsqueeze(0), embeddings)

    # Get top-k (excluding itself)
    top_indices = similarities.argsort(descending=True)[1:top_k+1]

    return [idx2word[i.item()] for i in top_indices]

