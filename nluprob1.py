def load_data(folder_path):
    documents = []

    for file in os.listdir(folder_path):
        if file.endswith(".txt"):
            with open(os.path.join(folder_path, file), 'r', encoding='utf-8') as f:
                text = f.read()
                documents.append(text)

    return documents


# -------------------------------
# STEP 2: Clean text
# -------------------------------
def clean_text(text):

    # Remove boilerplate patterns (menus, navigation, etc.)
    text = re.sub(r'\n+', ' ', text)  # remove extra newlines
    text = re.sub(r'\s+', ' ', text)  # remove extra spaces

    # Remove non-English characters
    text = re.sub(r'[^a-zA-Z\s]', ' ', text)

    # Lowercase
    text = text.lower()

    return text


# -------------------------------
# STEP 3: Tokenization
# -------------------------------
def tokenize_text(text):
    tokens = word_tokenize(text)

    # Remove very short tokens (like 'a', 'i')
    tokens = [word for word in tokens if len(word) > 2]

    return tokens


# -------------------------------
# STEP 4: Full preprocessing pipeline
# -------------------------------
def preprocess_corpus(documents):
    all_tokens = []
    cleaned_docs = []

    for doc in documents:
        clean_doc = clean_text(doc)
        tokens = tokenize_text(clean_doc)

        cleaned_docs.append(tokens)
        all_tokens.extend(tokens)

    return cleaned_docs, all_tokens


