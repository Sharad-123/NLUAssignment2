"""# ANALOGY TASK"""

def analogy(word_a, word_b, word_c, embeddings, word2idx, idx2word, top_k=5):

    if word_a not in word2idx or word_b not in word2idx or word_c not in word2idx:
        print("Word not in vocab")
        return

    vec = embeddings[word2idx[word_b]] - embeddings[word2idx[word_a]] + embeddings[word2idx[word_c]]

    similarities = F.cosine_similarity(vec.unsqueeze(0), embeddings)

    top_indices = similarities.argsort(descending=True)[1:top_k+1]

    return [idx2word[i.item()] for i in top_indices]

