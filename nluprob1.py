"""# MODELS : CBOW"""

import torch
import torch.nn as nn

class CBOW(nn.Module):
    def __init__(self, vocab_size, embed_dim):
        super().__init__()
        self.embeddings = nn.Embedding(vocab_size, embed_dim)
        self.linear = nn.Linear(embed_dim, vocab_size)

    def forward(self, context):
        embeds = self.embeddings(context)
        embeds = embeds.mean(dim=1)
        out = self.linear(embeds)
        return out

"""# Skip-gram with Negative Sampling"""

class SkipGramNS(nn.Module):
    def __init__(self, vocab_size, embed_dim):
        super().__init__()
        self.input_embeddings = nn.Embedding(vocab_size, embed_dim)
        self.output_embeddings = nn.Embedding(vocab_size, embed_dim)

    def forward(self, target, pos_context, neg_context):
        # Corrected indentation
        target_emb = self.input_embeddings(target)
        pos_emb = self.output_embeddings(pos_context)
        neg_emb = self.output_embeddings(neg_context)

        pos_score = torch.sum(target_emb * pos_emb, dim=1)
        # Re-applying fix: squeeze() instead of squeeze(2)
        neg_score = torch.bmm(neg_emb, target_emb.unsqueeze(2)).squeeze()

        # Re-applying fix: Removed dim=1 from torch.sum for neg_score
        loss = -torch.log(torch.sigmoid(pos_score)) \
               - torch.sum(torch.log(torch.sigmoid(-neg_score)))

        return loss.mean()

"""# CBOW Training"""

def train_cbow(data, model, epochs=5, lr=0.005):

    import random
    optimizer = torch.optim.Adam(model.parameters(), lr=lr)
    loss_fn = nn.CrossEntropyLoss()

    for epoch in range(epochs):
        total_loss = 0

        random.shuffle(data)

        for context, target in data:

            context = torch.tensor([context])
            target = torch.tensor([target])

            output = model(context)
            loss = loss_fn(output, target)

            optimizer.zero_grad()
            loss.backward()
            optimizer.step()

            total_loss += loss.item()

        print(f"Epoch {epoch+1}, Loss: {total_loss / len(data):.4f}")

"""# Skip-gram Training"""

def train_skipgram(data, model, vocab_size, neg_samples=5, epochs=5, lr=0.005):

    import random
    optimizer = torch.optim.Adam(model.parameters(), lr=lr)

    for epoch in range(epochs):
        total_loss = 0

        random.shuffle(data)

        for target, context in data:

            target = torch.tensor([target])
            context = torch.tensor([context])

            neg_context = torch.randint(0, vocab_size, (1, neg_samples))

            loss = model(target, context, neg_context)

            optimizer.zero_grad()
            loss.backward()
            optimizer.step()

            total_loss += loss.item()

        print(f"Epoch {epoch+1}, Loss: {total_loss / len(data):.4f}")

